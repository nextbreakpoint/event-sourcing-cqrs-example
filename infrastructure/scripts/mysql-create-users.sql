DROP USER IF EXISTS 'liquibase';
DROP USER IF EXISTS 'accounts-rw';
DROP USER IF EXISTS 'designs-rw';
CREATE USER IF NOT EXISTS 'liquibase' IDENTIFIED BY 'password' WITH MAX_CONNECTIONS_PER_HOUR 10 PASSWORD EXPIRE NEVER;
CREATE USER IF NOT EXISTS 'accounts-rw' IDENTIFIED BY 'password' WITH MAX_CONNECTIONS_PER_HOUR 1000 PASSWORD EXPIRE NEVER;
CREATE USER IF NOT EXISTS 'designs-rw' IDENTIFIED BY 'password' WITH MAX_CONNECTIONS_PER_HOUR 1000 PASSWORD EXPIRE NEVER;
GRANT ALL ON `accounts`.* TO 'liquibase';
GRANT ALL ON `designs`.* TO 'liquibase';
GRANT SELECT, INSERT, UPDATE, DELETE ON `accounts`.* TO 'accounts-rw';
GRANT SELECT, INSERT, UPDATE, DELETE ON `designs`.* TO 'designs-rw';
